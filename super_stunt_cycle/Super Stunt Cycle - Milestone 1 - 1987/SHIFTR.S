SHIFTRIGHT: 
	CLC			
	ROL	MTMASK	;SHIFT MASK 
	BCC	.SAMEPF ;IF CARRY SET GET NEW POINTER
	ROL	MTMASK
	LDX	PLAYF
	INX
	CPX	#MTNWIDTH+1
	BCC	.OK
	LDX	#0
.OK:	STX	PLAYF
	LDA	MTNPTRTABL,X
	STA	MOUNTPTR
	LDA	MTNPTRTABH,X
	STA	MOUNTPTR+1

.SAMEPF:
	LDX	#9

.SLOOP:
	TXA
	TAY

	LDA	@MOUNTPTR(Y) ;MOUNTPTR POINTS TO

	SEC
	AND	MTMASK
	BNE	.SETC
	CLC
.SETC:

	LDA	MTNPLAY0A,X
	BCC	.OV
	ROL
	ORA	#$10
	BNE	.OV1	;BR ALLWAYS
.OV:	ROL
	AND	#$EF
.OV1:	STA	MTNPLAY0A,X
	
	ROR	MTNPLAY1A,X
	ROL	MTNPLAY2A,X
	LDA	MTNPLAY0B,X
	BCC	.OVB
	ROL
	ORA	#$10
	STA	MTNPLAY0B,X
	ROR	MTNPLAY1B,X
	ROL	MTNPLAY2B,X
	DEX
	BPL	.SLOOP
	jmp 	.exit

.OVB:	ROL
	AND	#$EF
.OV1B:	STA	MTNPLAY0B,X
	ROR	MTNPLAY1B,X
	ROL	MTNPLAY2B,X
	DEX
	BPL	.SLOOP

.exit:
	RTS
