	STA	XPOSH2,Y
*
	LDX	YPOSH,Y
	LDA	YPOSH2,Y
	STA	YPOSH,Y
	TXA
	STA	YPOSH2,Y

	DEY
	BPL	SWCHLOOP

GIVBRTH2:
	LDX	PLAYER		;GIVE BIRTH, TWO PLAYER STYLE (PMSG)
	LDA	#BIRTH		;GIVE BIRTH
	STA	STATE,X
	LDA	#PMSGT+$20	;AT TIMER PMSGT+20
	STA	TIMER,X

	RTS
