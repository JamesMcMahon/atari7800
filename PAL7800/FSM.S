	LDX	XVELH,Y
	LDA	XVELH2,Y
	STA	XVELH,Y
	TXA
	STA	XVELH2,Y
*
	LDX	YVELH,Y
	LDA	YVELH2,Y
	STA	YVELH,Y
	TXA
	STA	YVELH2,Y
*
	LDX	XVELL,Y
	LDA	XVELL2,Y
	STA	XVELL,Y
	TXA
	STA	XVELL2,Y
*
	LDX	YVELL,Y
	LDA	YVELL2,Y
	STA	YVELL,Y
	TXA
	STA	YVELL2,Y
*
	LDX	XPOSH,Y
	LDA	XPOSH2,Y
	STA	XPOSH,Y
